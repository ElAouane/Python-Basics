
def add(num1, num2):
    return num1 + num2

def subtract(num1, num2):
    return num1 - num2

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return num1 / num2

def are_circle(r):

    return 3.14* (r *r)

def are_of_square(width, height):
    return width * height

def triangle_area(base, height):
    return height * base / height


